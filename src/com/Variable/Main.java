package com.Variable;

public class Main {
    public static void main (String [] args){

        int x = 10; //artinya variable tersebut bernilai 10 dan menggunakan tipe data Integer
        System.out.println("Nilai X = " +x);

        x = 20;
        System.out.println("Nilai x baru = " +x);

        /*
        variable = nilai nya bisa berubah-ubah
        x = identifier. nama identifier tidak boleh dengan nama yang sudah terdapat di java, contoh: static, void.

        contoh assignment (membuat nilai kedalam sebuah variable) => int a = 7;
        contoh deklarasi => int b;
         */
    }
}
